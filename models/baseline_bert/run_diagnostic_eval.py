"""Run the local baseline_bert error diagnostic over saved diagnostic predictions."""

import argparse
import subprocess
import sys
from pathlib import Path

from core.config import PREDICTIONS_DIR

SCRIPT_DIR = Path(__file__).resolve().parent
DATASET_NAME = "diagnostic_val"


def _discover_available_modes() -> list[str]:
    suffix = f"_{DATASET_NAME}_predictions.csv"
    return sorted(file_path.name[: -len(suffix)] for file_path in PREDICTIONS_DIR.glob(f"*{suffix}"))


def _prediction_path_for_mode(mode: str) -> Path:
    return PREDICTIONS_DIR / f"{mode}_{DATASET_NAME}_predictions.csv"


def _run_diagnostic(modes: list[str], python: str) -> None:
    diag_script = SCRIPT_DIR / "error_diagnostic.py"
    for mode in modes:
        cmd = [
            python,
            str(diag_script),
            "--mode",
            mode,
            "--dataset",
            DATASET_NAME,
        ]
        print(f"\n  Diagnostic: {' '.join(str(part) for part in cmd[2:])}")
        subprocess.run(cmd, cwd=str(SCRIPT_DIR.parent.parent), check=False)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Run the baseline_bert local error diagnostic using the "
            "diagnostic_val predictions already generated by evaluate.py or run_all_evals.py."
        )
    )
    parser.add_argument(
        "--mode",
        default="all",
        help=(
            "Mode to diagnose, or 'all' to auto-discover every mode that "
            f"already has a {DATASET_NAME} predictions file."
        ),
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()

    if args.mode == "all":
        modes = _discover_available_modes()
        if not modes:
            print(
                "[ERROR] No diagnostic prediction files found. "
                f"Expected files like *_{DATASET_NAME}_predictions.csv under {PREDICTIONS_DIR}."
            )
            sys.exit(1)
    else:
        modes = [args.mode]

    missing_modes = [mode for mode in modes if not _prediction_path_for_mode(mode).exists()]
    if missing_modes:
        print(
            "[ERROR] Missing diagnostic prediction files for: "
            f"{missing_modes}. Evaluate those modes first so "
            f"{DATASET_NAME} predictions exist under {PREDICTIONS_DIR}."
        )
        sys.exit(1)

    print(f"Modes     : {modes}")
    print(f"Predictions: {PREDICTIONS_DIR}")
    print(f"Python    : {sys.executable}")
    print(f"\n{'-' * 60}")
    print("Running error diagnostic")
    print(f"{'-' * 60}")
    _run_diagnostic(modes, sys.executable)

    print(f"\nDone. Results in: {SCRIPT_DIR / 'outputs' / 'error_diagnostic_results'}")


if __name__ == "__main__":
    main()
